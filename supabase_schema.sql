-- High-performance optimized function for dashboard stats
DROP FUNCTION IF EXISTS public.get_dashboard_service_stats(text, text, date, date);
CREATE OR REPLACE FUNCTION public.get_dashboard_service_stats(
    p_municipality text DEFAULT NULL,
    p_barangay text DEFAULT NULL,
    p_start_date date DEFAULT NULL,
    p_end_date date DEFAULT NULL
)
RETURNS json
SECURITY DEFINER
AS $$
DECLARE
    v_result json;
    v_sql text;
BEGIN
    v_sql := $query$
    WITH patient_first_services AS (
        SELECT 
            ps.patient_id,
            LOWER(p.municipality) as lmuni,
            LOWER(p.barangay) as lbrgy,
            MIN(CASE WHEN ps.nutrition THEN ps.date_of_service END) as first_nutrition_date,
            MIN(CASE WHEN ps.cancer THEN ps.date_of_service END) as first_cancer_date,
            MIN(CASE WHEN ps.immunization THEN ps.date_of_service END) as first_immunization_date,
            MIN(CASE WHEN ps.hpn THEN ps.date_of_service END) as first_hpn_date,
            MIN(CASE WHEN ps.dm THEN ps.date_of_service END) as first_dm_date,
            MIN(CASE WHEN ps.maternal_health THEN ps.date_of_service END) as first_maternal_health_date,
            MIN(CASE WHEN ps.road_safety THEN ps.date_of_service END) as first_road_safety_date,
            MIN(CASE WHEN ps.mental_health THEN ps.date_of_service END) as first_mental_health_date,
            MIN(CASE WHEN ps.tb THEN ps.date_of_service END) as first_tb_date,
            MIN(CASE WHEN ps.hiv THEN ps.date_of_service END) as first_hiv_date,
            MIN(CASE WHEN ps.wash THEN ps.date_of_service END) as first_wash_date,
            MIN(CASE WHEN ps.health_promotion THEN ps.date_of_service END) as first_health_promotion_date,
            MIN(CASE WHEN ps.fpe THEN ps.date_of_service END) as first_fpe_date,
            MIN(CASE WHEN ps.philhealth THEN ps.date_of_service END) as first_philhealth_date,
            MIN(CASE WHEN ps.referral THEN ps.date_of_service END) as first_referral_date,
            MIN(CASE WHEN ps.large_scale_pk_activity THEN ps.date_of_service END) as first_large_scale_pk_activity_date,
            MIN(ps.date_of_service) as absolute_first_service_date
        FROM patient_services ps
        JOIN patients p ON ps.patient_id = p.id
        WHERE 1=1 $query$;

    IF p_municipality IS NOT NULL THEN
        v_sql := v_sql || $query$ AND LOWER(p.municipality) = LOWER($1) $query$;
    END IF;

    IF p_barangay IS NOT NULL THEN
        v_sql := v_sql || $query$ AND LOWER(p.barangay) = LOWER($2) $query$;
    END IF;

    v_sql := v_sql || $query$
        GROUP BY ps.patient_id, LOWER(p.municipality), LOWER(p.barangay)
    ),
    patient_period_summary AS (
        SELECT 
            patient_id,
            lmuni,
            lbrgy,
            CASE WHEN ($3::date IS NULL OR first_nutrition_date >= $3::date) AND ($4::date IS NULL OR first_nutrition_date <= $4::date) THEN first_nutrition_date ELSE NULL END as first_nutrition_date,
            CASE WHEN ($3::date IS NULL OR first_cancer_date >= $3::date) AND ($4::date IS NULL OR first_cancer_date <= $4::date) THEN first_cancer_date ELSE NULL END as first_cancer_date,
            CASE WHEN ($3::date IS NULL OR first_immunization_date >= $3::date) AND ($4::date IS NULL OR first_immunization_date <= $4::date) THEN first_immunization_date ELSE NULL END as first_immunization_date,
            CASE WHEN ($3::date IS NULL OR first_hpn_date >= $3::date) AND ($4::date IS NULL OR first_hpn_date <= $4::date) THEN first_hpn_date ELSE NULL END as first_hpn_date,
            CASE WHEN ($3::date IS NULL OR first_dm_date >= $3::date) AND ($4::date IS NULL OR first_dm_date <= $4::date) THEN first_dm_date ELSE NULL END as first_dm_date,
            CASE WHEN ($3::date IS NULL OR first_maternal_health_date >= $3::date) AND ($4::date IS NULL OR first_maternal_health_date <= $4::date) THEN first_maternal_health_date ELSE NULL END as first_maternal_health_date,
            CASE WHEN ($3::date IS NULL OR first_road_safety_date >= $3::date) AND ($4::date IS NULL OR first_road_safety_date <= $4::date) THEN first_road_safety_date ELSE NULL END as first_road_safety_date,
            CASE WHEN ($3::date IS NULL OR first_mental_health_date >= $3::date) AND ($4::date IS NULL OR first_mental_health_date <= $4::date) THEN first_mental_health_date ELSE NULL END as first_mental_health_date,
            CASE WHEN ($3::date IS NULL OR first_tb_date >= $3::date) AND ($4::date IS NULL OR first_tb_date <= $4::date) THEN first_tb_date ELSE NULL END as first_tb_date,
            CASE WHEN ($3::date IS NULL OR first_hiv_date >= $3::date) AND ($4::date IS NULL OR first_hiv_date <= $4::date) THEN first_hiv_date ELSE NULL END as first_hiv_date,
            (first_nutrition_date IS NOT NULL AND ($3::date IS NULL OR first_nutrition_date >= $3::date) AND ($4::date IS NULL OR first_nutrition_date <= $4::date)) as nutrition,
            (first_cancer_date IS NOT NULL AND ($3::date IS NULL OR first_cancer_date >= $3::date) AND ($4::date IS NULL OR first_cancer_date <= $4::date)) as cancer,
            (first_immunization_date IS NOT NULL AND ($3::date IS NULL OR first_immunization_date >= $3::date) AND ($4::date IS NULL OR first_immunization_date <= $4::date)) as immunization,
            (first_hpn_date IS NOT NULL AND ($3::date IS NULL OR first_hpn_date >= $3::date) AND ($4::date IS NULL OR first_hpn_date <= $4::date)) as hpn,
            (first_dm_date IS NOT NULL AND ($3::date IS NULL OR first_dm_date >= $3::date) AND ($4::date IS NULL OR first_dm_date <= $4::date)) as dm,
            (first_maternal_health_date IS NOT NULL AND ($3::date IS NULL OR first_maternal_health_date >= $3::date) AND ($4::date IS NULL OR first_maternal_health_date <= $4::date)) as maternal_health,
            (first_road_safety_date IS NOT NULL AND ($3::date IS NULL OR first_road_safety_date >= $3::date) AND ($4::date IS NULL OR first_road_safety_date <= $4::date)) as road_safety,
            (first_mental_health_date IS NOT NULL AND ($3::date IS NULL OR first_mental_health_date >= $3::date) AND ($4::date IS NULL OR first_mental_health_date <= $4::date)) as mental_health,
            (first_tb_date IS NOT NULL AND ($3::date IS NULL OR first_tb_date >= $3::date) AND ($4::date IS NULL OR first_tb_date <= $4::date)) as tb,
            (first_hiv_date IS NOT NULL AND ($3::date IS NULL OR first_hiv_date >= $3::date) AND ($4::date IS NULL OR first_hiv_date <= $4::date)) as hiv,
            (first_wash_date IS NOT NULL AND ($3::date IS NULL OR first_wash_date >= $3::date) AND ($4::date IS NULL OR first_wash_date <= $4::date)) as wash,
            (first_health_promotion_date IS NOT NULL AND ($3::date IS NULL OR first_health_promotion_date >= $3::date) AND ($4::date IS NULL OR first_health_promotion_date <= $4::date)) as health_promotion,
            (first_fpe_date IS NOT NULL AND ($3::date IS NULL OR first_fpe_date >= $3::date) AND ($4::date IS NULL OR first_fpe_date <= $4::date)) as fpe,
            (first_philhealth_date IS NOT NULL AND ($3::date IS NULL OR first_philhealth_date >= $3::date) AND ($4::date IS NULL OR first_philhealth_date <= $4::date)) as philhealth,
            (first_referral_date IS NOT NULL AND ($3::date IS NULL OR first_referral_date >= $3::date) AND ($4::date IS NULL OR first_referral_date <= $4::date)) as referral,
            (first_large_scale_pk_activity_date IS NOT NULL AND ($3::date IS NULL OR first_large_scale_pk_activity_date >= $3::date) AND ($4::date IS NULL OR first_large_scale_pk_activity_date <= $4::date)) as large_scale_pk_activity,
            (absolute_first_service_date IS NOT NULL AND ($3::date IS NULL OR absolute_first_service_date >= $3::date) AND ($4::date IS NULL OR absolute_first_service_date <= $4::date)) as is_new_patient
        FROM patient_first_services
        WHERE 
            (first_nutrition_date IS NOT NULL AND ($3::date IS NULL OR first_nutrition_date >= $3::date) AND ($4::date IS NULL OR first_nutrition_date <= $4::date)) OR
            (first_cancer_date IS NOT NULL AND ($3::date IS NULL OR first_cancer_date >= $3::date) AND ($4::date IS NULL OR first_cancer_date <= $4::date)) OR
            (first_immunization_date IS NOT NULL AND ($3::date IS NULL OR first_immunization_date >= $3::date) AND ($4::date IS NULL OR first_immunization_date <= $4::date)) OR
            (first_hpn_date IS NOT NULL AND ($3::date IS NULL OR first_hpn_date >= $3::date) AND ($4::date IS NULL OR first_hpn_date <= $4::date)) OR
            (first_dm_date IS NOT NULL AND ($3::date IS NULL OR first_dm_date >= $3::date) AND ($4::date IS NULL OR first_dm_date <= $4::date)) OR
            (first_maternal_health_date IS NOT NULL AND ($3::date IS NULL OR first_maternal_health_date >= $3::date) AND ($4::date IS NULL OR first_maternal_health_date <= $4::date)) OR
            (first_road_safety_date IS NOT NULL AND ($3::date IS NULL OR first_road_safety_date >= $3::date) AND ($4::date IS NULL OR first_road_safety_date <= $4::date)) OR
            (first_mental_health_date IS NOT NULL AND ($3::date IS NULL OR first_mental_health_date >= $3::date) AND ($4::date IS NULL OR first_mental_health_date <= $4::date)) OR
            (first_tb_date IS NOT NULL AND ($3::date IS NULL OR first_tb_date >= $3::date) AND ($4::date IS NULL OR first_tb_date <= $4::date)) OR
            (first_hiv_date IS NOT NULL AND ($3::date IS NULL OR first_hiv_date >= $3::date) AND ($4::date IS NULL OR first_hiv_date <= $4::date)) OR
            (first_wash_date IS NOT NULL AND ($3::date IS NULL OR first_wash_date >= $3::date) AND ($4::date IS NULL OR first_wash_date <= $4::date)) OR
            (first_health_promotion_date IS NOT NULL AND ($3::date IS NULL OR first_health_promotion_date >= $3::date) AND ($4::date IS NULL OR first_health_promotion_date <= $4::date)) OR
            (first_fpe_date IS NOT NULL AND ($3::date IS NULL OR first_fpe_date >= $3::date) AND ($4::date IS NULL OR first_fpe_date <= $4::date)) OR
            (first_philhealth_date IS NOT NULL AND ($3::date IS NULL OR first_philhealth_date >= $3::date) AND ($4::date IS NULL OR first_philhealth_date <= $4::date)) OR
            (first_referral_date IS NOT NULL AND ($3::date IS NULL OR first_referral_date >= $3::date) AND ($4::date IS NULL OR first_referral_date <= $4::date)) OR
            (first_large_scale_pk_activity_date IS NOT NULL AND ($3::date IS NULL OR first_large_scale_pk_activity_date >= $3::date) AND ($4::date IS NULL OR first_large_scale_pk_activity_date <= $4::date)) OR
            (absolute_first_service_date IS NOT NULL AND ($3::date IS NULL OR absolute_first_service_date >= $3::date) AND ($4::date IS NULL OR absolute_first_service_date <= $4::date))
    ),
    activity_services AS (
        SELECT 
            ps.date_of_service,
            LOWER(p.municipality) as lmuni,
            LOWER(p.barangay) as lbrgy,
            BOOL_OR(ps.large_scale_pk_activity) as has_large_scale,
            BOOL_OR(ps.large_scale_pk_activity AND ps.nutrition) as ls_nutrition,
            BOOL_OR(ps.large_scale_pk_activity AND ps.cancer) as ls_cancer,
            BOOL_OR(ps.large_scale_pk_activity AND ps.immunization) as ls_immunization,
            BOOL_OR(ps.large_scale_pk_activity AND ps.hpn) as ls_hpn,
            BOOL_OR(ps.large_scale_pk_activity AND ps.dm) as ls_dm,
            BOOL_OR(ps.large_scale_pk_activity AND ps.maternal_health) as ls_maternal_health,
            BOOL_OR(ps.large_scale_pk_activity AND ps.road_safety) as ls_road_safety,
            BOOL_OR(ps.large_scale_pk_activity AND ps.mental_health) as ls_mental_health,
            BOOL_OR(ps.large_scale_pk_activity AND ps.tb) as ls_tb,
            BOOL_OR(ps.large_scale_pk_activity AND ps.hiv) as ls_hiv,
            BOOL_OR(ps.large_scale_pk_activity AND ps.wash) as ls_wash,
            BOOL_OR(ps.large_scale_pk_activity AND ps.health_promotion) as ls_health_promotion,
            BOOL_OR(ps.large_scale_pk_activity AND ps.fpe) as ls_fpe,
            BOOL_OR(ps.large_scale_pk_activity AND ps.philhealth) as ls_philhealth,
            BOOL_OR(ps.large_scale_pk_activity AND ps.referral) as ls_referral
        FROM patient_services ps
        JOIN patients p ON ps.patient_id = p.id
        WHERE 1=1 $query$;

    IF p_municipality IS NOT NULL THEN
        v_sql := v_sql || $query$ AND LOWER(p.municipality) = LOWER($1) $query$;
    END IF;

    IF p_barangay IS NOT NULL THEN
        v_sql := v_sql || $query$ AND LOWER(p.barangay) = LOWER($2) $query$;
    END IF;

    IF p_start_date IS NOT NULL THEN
        v_sql := v_sql || $query$ AND ps.date_of_service >= $3 $query$;
    END IF;

    IF p_end_date IS NOT NULL THEN
        v_sql := v_sql || $query$ AND ps.date_of_service <= $4 $query$;
    END IF;

    v_sql := v_sql || $query$
        GROUP BY ps.date_of_service, LOWER(p.municipality), LOWER(p.barangay)
    ),
    barangay_activities AS (
        SELECT 
            lmuni,
            lbrgy,
            COUNT(*)::bigint as pk_activities,
            COUNT(CASE WHEN has_large_scale THEN 1 END)::bigint as large_scale_activities,
            COUNT(CASE WHEN ls_nutrition THEN 1 END)::bigint as ls_nutrition,
            COUNT(CASE WHEN ls_cancer THEN 1 END)::bigint as ls_cancer,
            COUNT(CASE WHEN ls_immunization THEN 1 END)::bigint as ls_immunization,
            COUNT(CASE WHEN ls_hpn THEN 1 END)::bigint as ls_hpn,
            COUNT(CASE WHEN ls_dm THEN 1 END)::bigint as ls_dm,
            COUNT(CASE WHEN ls_maternal_health THEN 1 END)::bigint as ls_maternal_health,
            COUNT(CASE WHEN ls_road_safety THEN 1 END)::bigint as ls_road_safety,
            COUNT(CASE WHEN ls_mental_health THEN 1 END)::bigint as ls_mental_health,
            COUNT(CASE WHEN ls_tb THEN 1 END)::bigint as ls_tb,
            COUNT(CASE WHEN ls_hiv THEN 1 END)::bigint as ls_hiv,
            COUNT(CASE WHEN ls_wash THEN 1 END)::bigint as ls_wash,
            COUNT(CASE WHEN ls_health_promotion THEN 1 END)::bigint as ls_health_promotion,
            COUNT(CASE WHEN ls_fpe THEN 1 END)::bigint as ls_fpe,
            COUNT(CASE WHEN ls_philhealth THEN 1 END)::bigint as ls_philhealth,
            COUNT(CASE WHEN ls_referral THEN 1 END)::bigint as ls_referral
        FROM activity_services
        GROUP BY lmuni, lbrgy
    ),
    program_stats_calc AS (
        SELECT
            SUM(CASE WHEN nutrition THEN 1 ELSE 0 END)::int as nutrition,
            SUM(CASE WHEN cancer THEN 1 ELSE 0 END)::int as cancer,
            SUM(CASE WHEN immunization THEN 1 ELSE 0 END)::int as immunization,
            SUM(CASE WHEN hpn THEN 1 ELSE 0 END)::int as hpn,
            SUM(CASE WHEN dm THEN 1 ELSE 0 END)::int as dm,
            SUM(CASE WHEN maternal_health THEN 1 ELSE 0 END)::int as maternal_health,
            SUM(CASE WHEN road_safety THEN 1 ELSE 0 END)::int as road_safety,
            SUM(CASE WHEN mental_health THEN 1 ELSE 0 END)::int as mental_health,
            SUM(CASE WHEN tb THEN 1 ELSE 0 END)::int as tb,
            SUM(CASE WHEN hiv THEN 1 ELSE 0 END)::int as hiv,
            SUM(CASE WHEN wash THEN 1 ELSE 0 END)::int as wash,
            SUM(CASE WHEN health_promotion THEN 1 ELSE 0 END)::int as health_promotion,
            SUM(CASE WHEN fpe THEN 1 ELSE 0 END)::int as fpe,
            SUM(CASE WHEN philhealth THEN 1 ELSE 0 END)::int as philhealth,
            SUM(CASE WHEN referral THEN 1 ELSE 0 END)::int as referral,
            SUM(CASE WHEN is_new_patient THEN 1 ELSE 0 END)::int as total_population_reached,
            SUM(CASE WHEN large_scale_pk_activity THEN 1 ELSE 0 END)::int as total_large_scale_clients_served,
            SUM(CASE WHEN large_scale_pk_activity AND (nutrition OR cancer OR immunization OR hpn OR dm OR maternal_health OR road_safety OR mental_health OR tb OR hiv OR wash) THEN 1 ELSE 0 END)::int as total_priority_large_scale_patients
        FROM patient_period_summary
    ),
    large_scale_details AS (
        SELECT
            COALESCE(SUM(ba.pk_activities), 0) as total_pk_activities,
            COALESCE(SUM(ba.large_scale_activities), 0) as total_large_scale_activities,
            COALESCE(SUM(ba.ls_nutrition), 0) as ls_nutrition,
            COALESCE(SUM(ba.ls_cancer), 0) as ls_cancer,
            COALESCE(SUM(ba.ls_immunization), 0) as ls_immunization,
            COALESCE(SUM(ba.ls_hpn), 0) as ls_hpn,
            COALESCE(SUM(ba.ls_dm), 0) as ls_dm,
            COALESCE(SUM(ba.ls_maternal_health), 0) as ls_maternal_health,
            COALESCE(SUM(ba.ls_road_safety), 0) as ls_road_safety,
            COALESCE(SUM(ba.ls_mental_health), 0) as ls_mental_health,
            COALESCE(SUM(ba.ls_tb), 0) as ls_tb,
            COALESCE(SUM(ba.ls_hiv), 0) as ls_hiv,
            COALESCE(SUM(ba.ls_wash), 0) as ls_wash,
            COALESCE(SUM(ba.ls_health_promotion), 0) as ls_health_promotion,
            COALESCE(SUM(ba.ls_fpe), 0) as ls_fpe,
            COALESCE(SUM(ba.ls_philhealth), 0) as ls_philhealth,
            COALESCE(SUM(ba.ls_referral), 0) as ls_referral
        FROM barangay_activities ba
    ),
    muni_stats AS (
        SELECT
            lmuni as muni,
            (
                SUM(CASE WHEN nutrition THEN 1 ELSE 0 END) +
                SUM(CASE WHEN cancer THEN 1 ELSE 0 END) +
                SUM(CASE WHEN immunization THEN 1 ELSE 0 END) +
                SUM(CASE WHEN hpn THEN 1 ELSE 0 END) +
                SUM(CASE WHEN dm THEN 1 ELSE 0 END) +
                SUM(CASE WHEN maternal_health THEN 1 ELSE 0 END) +
                SUM(CASE WHEN road_safety THEN 1 ELSE 0 END) +
                SUM(CASE WHEN mental_health THEN 1 ELSE 0 END) +
                SUM(CASE WHEN tb THEN 1 ELSE 0 END) +
                SUM(CASE WHEN hiv THEN 1 ELSE 0 END)
            )::int as served,
            SUM(CASE WHEN wash THEN 1 ELSE 0 END)::int as households_served,
            SUM(CASE WHEN is_new_patient THEN 1 ELSE 0 END)::int as population_reached
        FROM patient_period_summary
        GROUP BY lmuni
